export function flag(code) {
  if (!code || typeof code !== "string" || code.length !== 2) return "🌍";
  const A = 0x1f1e6;
  return String.fromCodePoint(
    A + code.toUpperCase().charCodeAt(0) - 65,
    A + code.toUpperCase().charCodeAt(1) - 65
  );
}

